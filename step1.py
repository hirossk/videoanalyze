import streamlit as st

# --- ステップ1：画面に文字や部品を表示する ---

# 1. ページのタイトル（一番大きな見出し）を表示
st.title("🎈 はじめてのWebアプリ")

# 2. 簡単な説明を表示
st.write("名前を入れてボタンを押してみてね！")

# 3. 名前を入力するためのテキストボックスを作成
# 入力された名前は `name` という名前の「いれもの」に保存される
name = st.text_input("ここに名前を入力してください")


# --- ステップ2：ボタンを押したら反応するようにする ---

# 4. 「あいさつ」ボタンを作成
# st.button()は、押されると True (「はい、押されました」の合図)を返す
if st.button("あいさつする"):
    # 5. もしボタンが押されたら、以下の処理を実行する
    
    # 「いれもの」に名前が入っているかチェック
    if name:
        # 名前が入っていれば、挨拶を表示
        st.success(f"{name}さん、こんにちは！")
    else:
        # 何も入っていなければ、注意メッセージを表示
        st.warning("名前が入力されていません。")