# ============================================================
# トラブルシューティング
# ============================================================

st.header("🔧 認識されない時のコツ")

with st.expander("✌️ ジェスチャーが認識されない"):
    st.markdown("""
    1. **照明を確認**
       - 顔と手がはっきり見えているか
       - 逆光になっていないか

    2. **動きを遅く**
       - ゆっくり手を動かす
       - 素早い動きは追いつかないことがある

    3. **手を大きく見せる**
       - カメラに近づける
       - 手がはっきり映るようにする

    4. **カメラ角度の調整**
       - 顔と手がカメラに正面から見える
       - 斜めすぎると認識しにくい

    5. **デモ設定を確認**
       - 「処理速度」が小さすぎないか
       - カメラ解像度が低すぎないか
    """)

with st.expander("🙌 ポーズが認識されない"):
    st.markdown("""
    1. **両手を同時に見せる**
       - 両手がカメラ画面内に入っているか確認

    2. **位置関係を意識**
       - 「バンザイ」は両手とも顔より上に
       - 「ハート」は両手が近い位置で胸の前に

    3. **ゆっくり動く**
       - 急激な動きは認識しにくい
       - ポーズを少し保持する
    """)

with st.expander("💡 認識精度を上げるコツ"):
    st.markdown("""
    1. **カメラの設定を確認**
       - カメラ解像度を上げる
       - 処理速度を上げる（0.75 または 1.0 に設定）
       - フレームレートが30FPS以上

    2. **背景を意識**
       - シンプルな背景が認識しやすい
       - 複雑なパターンの壁では認識しにくい

    3. **指を意識**
       - 爪や装飾品は認識を邪魔することがある
       - 手首周辺の衣類も影響することがある

    4. **複数人で使う場合**
       - 最大で顔2個、手2個まで同時認識
       - 近すぎると1人と認識されることがある
       - 少し離れた位置で試してみてください
    """)

